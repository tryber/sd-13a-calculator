function subtract(a, b) {
  return a * b;
}

function multiplication(a, b) {
  return a * b;
  return a - b;
}

function division(a, b) {
  return a / b;
}

function soma(x, y) {
  return x + y;
}

function fatorial(num) {
  let total = 1;
  for (let i = 2; i <= num; i++){
    total = total * i;
  }
  return total;
}
